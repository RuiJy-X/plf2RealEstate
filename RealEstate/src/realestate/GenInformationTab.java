    public String getSellOrRent(){
        
        if ((rentButton.isSelected()) && (sellButton.isSelected())){
            rentOrSell = "null";
            
        }else if (rentButton.isSelected()){
            rentOrSell = "rent";
           
        }else if(sellButton.isSelected()){
      
            rentOrSell = "sell";
            
        }
        
        else{
            rentOrSell = "null";
        }
        return rentOrSell;
    }

